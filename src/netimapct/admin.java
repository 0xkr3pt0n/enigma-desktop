    private void adminLogin_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminLogin_btnActionPerformed
        String admin_username = user_input.getText();
        String admin_password = adminPassword_input.getText();
        if(!admin_username.isEmpty() && !admin_password.isEmpty()){
            int is_valid = validate_login_admin(admin_username, admin_password);
            if (is_valid == 2){
                //case first time open
                JOptionPane.showMessageDialog(this, "Welcome to Enigma secure communication application.");
            }
            else if (is_valid == 1){
                // case admin user and password is correct
                login_succsess(admin_username);
            }
            else{
                // case invalid username or password 
                JOptionPane.showMessageDialog(this, "Invalid username or password.");
            }
        }else{
            // case blank username or password
            JOptionPane.showMessageDialog(this, "username or password cannot be blank");
        }
        
    }//GEN-LAST:event_adminLogin_btnActionPerformed

