    private int validate_login_admin(String username, String password){
        int result = -1;
        try{
            PreparedStatement stmt = con.prepareStatement("SELECT * FROM netpackt.admins");
            ResultSet rs = stmt.executeQuery();
        
            if (rs.next()) {
                // an admin already registerd case
                try{
                    PreparedStatement stmt_admin_login = con.prepareStatement("SELECT password FROM netpackt.admins where username = ?");
                    stmt_admin_login.setString(1, username);
                    ResultSet adminlogin_set = stmt_admin_login.executeQuery();
                    if (adminlogin_set.next()){
                        String storedPassword_admin = adminlogin_set.getString("password");
                        String hashedpassword = MD5Hashing.md5(password);
                        if (hashedpassword.equals(storedPassword_admin)) {
                            result = 1; // Valid login
                        } else {
                            result = 0; // Invalid password
                        }
                    }else{
                        result = -1; // admin not found
                    }
                } catch(SQLException ex){
            Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
        }

            } 
            else {
                // no admins found (case of first time open)
                try{
                PreparedStatement stmt_inserAdmin = con.prepareStatement("INSERT INTO netpackt.admins (username, password) values (?,?)");
                stmt_inserAdmin.setString(1, username);
                String hashedPassword = MD5Hashing.md5(password);
                stmt_inserAdmin.setString(2, hashedPassword);
                stmt_inserAdmin.executeUpdate();
                result = 2;
                } catch(SQLException ex){
                Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
                }    
            }

            // Close the ResultSet, PreparedStatement, and Connection
            rs.close();
            stmt.close();
        } catch(SQLException ex){
            Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
        }
        return result;    // 2 for first time login , 1 for valid login , 0 for invalid login 
    }
